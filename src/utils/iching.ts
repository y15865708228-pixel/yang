export function getDailyMotivation(dayOfYear: number): { hexagram: string; quote: string } {
  const hexIdx = dayOfYear % 64;
  const lineIdx = Math.floor(dayOfYear / 64); // 0-5, 最多366/64≈5.7
  const quote = QUOTES[hexIdx]?.[lineIdx] ?? QUOTES[hexIdx][0];
  return {
    hexagram: HEXAGRAM_NAMES[hexIdx],
    quote,
  };
}
